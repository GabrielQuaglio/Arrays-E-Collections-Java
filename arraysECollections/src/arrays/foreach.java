package arrays;

public class foreach {
public static void main(String[] args) {
	
	//forma avançada de percorrer um array
	//com uma nova estrutura de cintrole muito usada em arrays
	//muito usado pois queremos percorrer todos os elementos do primeiro ao ultimo
	
	
	double[] notas = {1,2,3,4,5};
	
	for(double nota: notas) {
		System.out.println(nota);
	}
	
	
}
	
}
